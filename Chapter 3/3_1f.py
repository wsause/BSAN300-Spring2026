# augmented assignment operations - Combining the assignment
# symbol with arithmetic and concatenation operators
a = 17
s = "hi"

a += 3      # Equivalent to a = a + 3
print(a)
a -= 3      # Equivalent to a = a - 3
print(a)
a *= 3      # Equivalent to a = a * 3
print(a)
a /= 3      # Equivalent to a = a / 3
print(a)
a %= 3      # Equivalent to a = a % 3
print(a)
s += " there"   # Equivalent to s = s + " there"
print(s)